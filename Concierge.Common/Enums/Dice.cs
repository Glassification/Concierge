namespace Concierge.Common.Enums
{
    /// <summary>
    /// Represents different types of dice.
    /// </summary>
    public enum Dice
    {
        /// <summary>
        /// Represents no dice.
        /// </summary>
        None = 0,

        /// <summary>
        /// Represents a 2-sided die [A coin toss].
        /// </summary>
        D2 = 2,

        /// <summary>
        /// Represents a 4-sided die.
        /// </summary>
        D4 = 4,

        /// <summary>
        /// Represents a 6-sided die.
        /// </summary>
        D6 = 6,

        /// <summary>
        /// Represents an 8-sided die.
        /// </summary>
        D8 = 8,

        /// <summary>
        /// Represents a 10-sided die.
        /// </summary>
        D10 = 10,

        /// <summary>
        /// Represents a 12-sided die.
        /// </summary>
        D12 = 12,

        /// <summary>
        /// Represents a 20-sided die.
        /// </summary>
        D20 = 20,

        /// <summary>
        /// Represents a 100-sided die.
        /// </summary>
        D100 = 100,
    }
}
